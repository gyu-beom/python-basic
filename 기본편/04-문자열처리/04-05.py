# 탈출문자

'''
- 탈출 문자
역슬래시 (\) 와 특정 문자 (숫자) 의 조합으로 표현할 수 없는 기능이나 문자를 표시

큰 따옴표 : \"
작은 따옴표 : \'
역슬래시 : \\
줄바꿈 : \n
그대로 출력 : r
커서를 맨 앞으로 이동 : \r
앞 글자 하나 삭제 : \b
'''

print('백문이 불여일견 백견이 불여일타')
print('백문이 불여일견\n백견이 불여일타')

print('저는 \'나도코딩\'입니다.')

print('C:\\Users\\Nadocoding\\Desktop\\PythonWorkSpace>')
