# 연산자

'''
- 산술 연산자
+ : 더하기
- : 빼기
* : 곱하기
/ : 나누기 (실수형 반환)
% : 나머지
// : 몫
** : 거듭 제곱

- 비교 연산자
> : 크다
>= : 크거나 같다
< : 작다
<= : 작거나 같다
== : 같다
!= : 같지 않다

- 논리 연산자
and : 둘다 참이면 True
or : 하나라도 참이면 True
not : 반전

- 멤버 연산자
in : 포함
not in : 미포함
'''

print('c' in 'cat') # True
print('c' not in 'cat') # False