# range

'''
- range
range(stop) : 0 부터 stop 미만에 해당되는 숫자
range(start, stop) : start 이상 stop 미만
range(start, stop, step) : start 이상 stop 미만 step 만큼 증가
'''

for x in range(10):
    print(x)

for x in range(1, 10):
    print(x)

for x in range(1, 10, 2):
    print(x)
for x in range(2, 10, 2):
    print(x)
for x in range(3, 10, 3):
    print(x)